#include <string.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*dst;

	i = 0;
	dst = s;
	while (i < n)
	{
		*dst = c;
		dst++;
		i++;
	}
	return (s);
}

